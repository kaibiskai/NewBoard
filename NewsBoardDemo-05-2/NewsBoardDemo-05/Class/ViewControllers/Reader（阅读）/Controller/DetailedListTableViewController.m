- (void)viewDidLoad {
    [super viewDidLoad];
    
//    [(KindListViewController *)self.parentViewController  ]
    
    KindListViewController  *vc = (KindListViewController *)self.parentViewController;
    
    [vc setBlock:^(NSInteger row) {
        self.selecteRow = row;
        [self.tableView reloadData];
        
    }];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
